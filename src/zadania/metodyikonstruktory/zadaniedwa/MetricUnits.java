package zadania.metodyikonstruktory.zadaniedwa;

class MetricUnits {
    double metersToCentimeters(double metres){
        return metres * 100;
    }

    double metersToMillimeters(double metres){
        return metres * 1000;
    }

    double centimetersToMeters(double centimetres){
        return centimetres / 100;
    }

    double millimetersToMeters(double millimeters){
        return millimeters / 1000;
    }
}
